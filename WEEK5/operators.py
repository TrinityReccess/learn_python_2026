
first_number = 10
second_number = 20

# Addition
print(first_number + second_number)

# Product
print(first_number * second_number)

# Quotient or Division
print(first_number / second_number)

# Modulus
print(first_number % second_number)

# Exponential
print(2 ** 2)
print(first_number ** second_number)


# Comparison Operators


print(f"{first_number} == {second_number} is {first_number == second_number}")

# not equal !=
print(f"{first_number} != {second_number} is {first_number != second_number}")

# Greater than >
print(f"{first_number} > {second_number} is {first_number > second_number}")

# Less than <
print(f"{first_number} < {second_number} is {first_number < second_number}")


# Greater than or equal to >=
print(f"{first_number} >= {second_number} is {first_number >= second_number}")

# Less than or equal to <=
print(f"{first_number} <= {second_number} is {first_number <= second_number}")


